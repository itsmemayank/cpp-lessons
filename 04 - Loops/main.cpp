#include <iostream>
using namespace std;

int main()
{
    int i;
    cout << "For Loop: " << endl;
    for (i = 0; i < 10; i++)
    {
        cout << i << endl;
    }

    cout << endl; // Blank Space

    int j = 0;
    cout << "While Loop: " << endl;
    while (j < 10)
    {
        cout << j << endl;
        j++;
    }

    return 0;
}
